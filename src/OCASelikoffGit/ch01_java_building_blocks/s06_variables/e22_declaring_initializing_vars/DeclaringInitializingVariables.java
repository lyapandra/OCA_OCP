/**
 * Chapter 1: Java Building Blocks<br/>
 * Declaring and Initializing Variables<br/>
 * page 25
 */
public class DeclaringInitializingVariables {
	public static void main(String[] args) {
		// declaring variables
		String zooName;
		int numberAnimals;

		// initializing variables
		zooName = "The Best Zoo";
		numberAnimals = 100;

		// declaring and initializing variables in one line
		String zooName2 = "The Best Zoo";
		int numberAnimals2 = 100;
	}
}
